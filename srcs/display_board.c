#include "header.h"

void	display_board(char board[32][32])
{
	int		x;
	int		y;

	x = 0;
	while (x < 32)
	{
		y = 0;
		while (y < 32)
		{
			write(1, &board[x][y], 1);
			if (y != 30)
				write(1, " ", 1);
			y++;
		}
		write(1, "\n", 1);
		x++;
	}
}
