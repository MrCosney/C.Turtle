void	check_teleport(char *way, int dir, int *x, int *y, int i)
{
	if (way[i] == 'g' && dir == 0)
    {
        if (*x == 30)
            *x = 0;
        *x = *x + 1;
    }
    else if (way[i] == 'g' && dir == 1)
    {
        if (*y == 30)
            *y = 0;
        *y = *y + 1;
    }
    else if (way[i] == 'g' && dir == 2)
    {
        if (*x == 1)
            *x = 30;
        *x = *x - 1;
    }
    else if (way[i] == 'g' && dir == 3)
    {
        if (*y == 1)
            *y = 30;
        *y = *y - 1;
    }
}
