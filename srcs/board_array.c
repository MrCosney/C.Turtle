void	board_array(char board[32][32])
{
	int l;
	int x;

	x = 0;
	while (x <= 31)
	{
		board[0][x] = '#';
		board[31][x] = '#';
		x++;
	}
	l = 1;
	while (l <= 30)
	{
		x = 1;
		while (x <= 30)
		{
    		board[l][0] = '#';
	    	board[l][x] = '.';
		    if (x == 15 && l == 15)
		        board[15][15] = 'o';
			x++;
		}
		board[l][31] = '#';
		l++;
	}
}
